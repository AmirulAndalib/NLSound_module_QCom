нанести аппаратные повреждения в случае
неаккуратного пользования устройством
(например, слишком высокая громкость), а
также приводить к бесконечному запуску.
В то же время, эти параметры весьма
эффективны и сильно влияют на звучание
вашего смартфона. Будьте аккуратны и
помните, что мы не несём никакой
ответственности за ваши действия.

