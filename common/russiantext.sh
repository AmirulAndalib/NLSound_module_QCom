Рекомендуется к установке только если выбор
данного пункта не приведет к отсутствию 
звука.

4. Аналогичен третьему, но так же удаляет
ограничения по выводу звука через динамики
устройства. Крайне небезопасный пункт,
может привести к повреждению динамиков на
высокой громкости, убирая все ограничения
на НЧ.

 ПРЕДУПРЕЖДЕНИЕ:
